import 'package:flutter_test/flutter_test.dart';
import 'package:kaede_toolbox/logic/prompt_builder.dart';

void main() {
  group('fmtSlashDate', () {
    test('スラッシュ区切りに変換する', () {
      expect(fmtSlashDate('2026-06-21'), '2026/06/21');
      expect(fmtSlashDate(''), '');
    });
  });

  group('buildNormalPrompt', () {
    test('タイトル未入力ならエラー', () {
      final r = buildNormalPrompt(const ThumbInput());
      expect(r.isError, isTrue);
      expect(r.text, 'メインタイトルまたはサブタイトルを入力してください。');
    });

    test('全項目入りで HTML 版と同一のプロンプトになる', () {
      final r = buildNormalPrompt(const ThumbInput(
        mainTitle: 'TRCゆる部',
        subTitle: 'ナイトレース',
        eventSub: 'スイッチさんお誕生日おめでとうレース',
        contents: ['ヒート戦', '総当たり戦'],
        date: '2026-06-13',
        charText: 'TRCゆる部',
        extraNote: 'ハート多め',
        qualityMode: true,
      ));
      expect(r.isError, isFalse);
      expect(r.text, '''【サムネイル画像 編集プロンプト】

添付した参考画像（TRCゆる部ナイトレース サムネイル）をベースに、画像編集（image-to-image）で以下の指示に従い、同じ画風・同じレイアウト構成で左側のテキストエリアのみを差し替えてください。

■ 絶対に変更しないもの（参考画像から完全に維持）
・右側のキャラクターイラスト（ピンクツインテールの女の子、猫耳型ヘッドホン、DJミキサー、ぶたのぬいぐるみ、ノートPC、表情・ポーズ・衣装・配色）は、下記7で指定する文字部分（ヘッドホン・ノートPC画面・DJ機材の表示文字）以外、一切変更せず、そのまま維持してください。
・背景の配色（濃いピンク〜マゼンタの放射状グラデーション）、キラキラの粒子・星・ハートが散りばめられた装飾スタイルも同じように踏襲してください。
・画像全体のアスペクト比（縦横比）は参考画像のまま維持し、トリミングや余白追加は行わないでください。

■ 差し替えるテキストエリア（画面左側）
上から下へ、以下の内容・スタイルで再構成してください。

1. メインタイトル（最上部・比較的小さめ、ピンク〜ゴールドの斜めグラデーション文字、太い白〜金の縁取り＋ドロップシャドウ、近くに小さなリボン/ハートの装飾）：
　　「TRCゆる部」

2. サブタイトル＝レース名（メインタイトルのすぐ下・最も大きい見出し文字、水色〜ピンクのグラデーション、太いアウトラインのぷっくりしたバブルレター、光沢ハイライト付き）：
　　「ナイトレース」

3. イベント副題（メインタイトルとレース内容の間・星アイコン「☆」で両端を挟んだ1行・ピンク文字・中央寄せ）：
　　☆～スイッチさんお誕生日おめでとうレース～☆
4. レース内容（音符「♪」と星「☆」のアイコンで両側を装飾した行。複数ある場合は縦に並べ、下に点線の区切り線を入れる）：
　　♪ ☆ ヒート戦 ☆ ♪
　　♪ ☆ 総当たり戦 ☆ ♪

5. 日付（テキストブロックの一番下・最も大きいサイズの数字、紫〜ピンクのグラデーション、太いバブル体の立体的な数字）：
　　「2026/06/13」

6. 日付の下にハート柄の飾りスクロールライン（参考画像と同じ、両端がくるんと丸まった罫線装飾）を配置してください。

7. キャラクター周りの文字（ヘッドホン・ノートPC画面・DJミキサー等の機材に表示されている文字。この部分のみ変更可）：
　　「TRCゆる部」
　　（フォント・配置・サイズ感は参考画像のスタイルを踏襲し、各箇所の文字内容をすべてこの文字に差し替えてください）

■ 全体トーン
ポップでキュートな配信告知サムネイルとして、キラキラ・グリッター・星・ハートを画面全体に散りばめ、文字はすべて太い縁取り＋ドロップシャドウ付きのぷっくりしたバブルレターで統一してください。

■ その他の指定
ハート多め

■ 同時に画質も向上させる
上記のテキスト差し替えと同時に、画像全体の解像度・描き込みクオリティも引き上げてください。
・線画やディテールの解像度を上げ、輪郭のガタつきやノイズを解消する
・グラデーションやハイライトを滑らかにする
・髪・リボン・衣装・ぬいぐるみ・機材の質感やディテールをより緻密に描き込む
・全体的なノイズやにじみを除去し、高解像度でクリアな仕上がりにする
（ただし、キャラクターのデザイン・ポーズ・配色・構図は変更しないこと）''');
    });

    test('副題なし・内容なし・画質OFFの分岐', () {
      final r = buildNormalPrompt(const ThumbInput(
        mainTitle: 'タイトル',
        qualityMode: false,
      ));
      expect(r.isError, isFalse);
      expect(r.text, contains('3. イベント副題：今回は指定なし。この行自体を省略してください。\n4. レース内容'));
      expect(r.text, contains('　　（レース内容の指定なし）'));
      expect(r.text, contains('「（日付未入力）」'));
      expect(r.text, contains('「（指定なし・参考画像のまま）」'));
      expect(r.text, isNot(contains('■ 同時に画質も向上させる')));
      expect(r.text, isNot(contains('■ その他の指定')));
      // 画質OFFなら全体トーンの行で終わる
      expect(r.text, endsWith('ぷっくりしたバブルレターで統一してください。'));
    });
  });

  group('buildBadgePrompt', () {
    test('全欄空ならエラー', () {
      final r = buildBadgePrompt(const ThumbInput());
      expect(r.isError, isTrue);
      expect(r.text, 'バッジの文字を少なくとも1つ入力してください。');
    });

    test('入力ありで HTML 版と同一のプロンプトになる', () {
      final r = buildBadgePrompt(const ThumbInput(
        badgeLine1: 'なごネット',
        badgeLine2: '中日新聞D4チケ戦',
        badgeDate: '2026/06/21',
        qualityMode: true,
      ));
      expect(r.isError, isFalse);
      expect(r.text, '''【バッジ内テキストのみ 差し替えプロンプト】

添付した画像をベースに、画像編集（image-to-image）で以下の指示に従い、画像内の「バッジ」部分のテキストだけを差し替えてください。それ以外の要素は一切変更しないでください。

■ 絶対に変更しないもの
・バッジ以外の画像全体（キャラクターイラスト、背景の配色・装飾、テキストバナーなど）は、添付画像から一切変更せず、そのまま維持してください。
・バッジの装飾要素（ティアラのアイコン、左右のリボン、下部の虹色の帯、バッジ全体のキラキラした縁取り・グラデーション背景）も、形状・色・配置ともに一切変更しないでください。
・バッジのサイズ・位置も変更しないでください。
・画像全体のアスペクト比（縦横比）は添付画像のまま維持し、トリミングや余白追加は行わないでください。

■ 差し替えるもの（バッジ内の文字のみ）
バッジ内の文字のフォントスタイル（縁取り・グラデーション・太さ）、行数（3行構成）は添付画像のまま維持し、各行の文字内容だけを以下に置き換えてください。

　1行目（上段の小さいテキスト）：「なごネット」
　2行目（中央の大きいテキスト・大会名/イベント名）：「中日新聞D4チケ戦」
　3行目（下段の日付）：「2026/06/21」

・各行の文字数が変わって文字サイズや折り返しの微調整が必要になっても構いませんが、その場合もバッジの外枠サイズと装飾は変えないでください。

■ 同時に画質も向上させる
上記のテキスト差し替えと同時に、バッジ以外の部分も含めて画像全体の解像度・描き込みクオリティを引き上げてください。
・線画やディテールの解像度を上げ、輪郭のガタつきやノイズを解消する
・グラデーションやハイライトを滑らかにする
・全体的なノイズやにじみを除去し、高解像度でクリアな仕上がりにする
（ただし、キャラクターやレイアウトなど、テキスト以外のデザインは一切変更しないこと）''');
    });

    test('一部空欄はプレースホルダになり、画質OFFで末尾ブロックなし', () {
      final r = buildBadgePrompt(const ThumbInput(
        badgeLine2: '大会名',
        qualityMode: false,
      ));
      expect(r.text, contains('　1行目（上段の小さいテキスト）：「（指定なし・添付画像のまま）」'));
      expect(r.text, contains('　2行目（中央の大きいテキスト・大会名/イベント名）：「大会名」'));
      expect(r.text, endsWith('バッジの外枠サイズと装飾は変えないでください。'));
    });
  });
}
